let elBtnSh1 = document.querySelector(".bsh1");
let elCpo = document.querySelector(".cpo_depart");

elBtnSh1.addEventListener("click", function(){

    if(elCpo.style.display == 'none') {
        elCpo.style.display = 'flex'
        elBtnSh1.classList.add("rotated");
    } else {
        elCpo.style.display = 'none'
        elBtnSh1.classList.remove("rotated");
    }
})

